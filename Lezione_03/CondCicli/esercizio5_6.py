# 1 condizione
if n < 2:

    print("You are a baby!")

# 2 condizione
elif n >= 2 and n < 4:

    print("You're a toddler!")

# 3 condizione
elif n >= 4 and n < 13:

    print("You're a kid!")

# 4 condizione
elif n >= 13 and n < 20:

    print("You're a teenager!")

# 5 condizione
elif n >= 20 and n < 65:

    print("You're an adult!")

# 6 condizione
elif n >= 65 and n < 110:

    print("You're an elder!")

