numbers: list[int] = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print("-----------------------------------------------------------------------")

for n in numbers:

    if n >= 4:

        print(f"{n}th")
    
    elif n == 3:

        print(f"{n}rd")

    elif n == 2:

        print(f"{n}nd")

    else:

        print(f"{n}st")

    print("-----------------------------------------------------------------------")